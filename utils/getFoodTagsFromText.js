export default function getFoodTagsFromText(text) {
  const tagMap = {
    // 主食類別
    飯: ["飯", "想吃飯", "飯類", "便當", "米食", "丼飯", "米飯"],
    麵: ["麵", "想吃麵", "麵類", "拉麵", "烏龍麵", "炒麵"],
    湯: ["湯", "來點湯", "熱湯", "喝湯", "湯的", "湯品"],
    粥: ["粥", "想吃粥", "粥品", "廣東粥"],
    麵包: ["麵包", "三明治", "可頌", "漢堡", "包子"],

    // 料理類型
    台式: ["台式", "台灣味", "小吃", "夜市", "台灣"],
    日式: ["日式", "日本", "壽司", "丼", "烏龍", "味噌"],
    韓式: ["韓式", "韓國", "泡菜", "韓國料理"],
    港式: ["港式", "港點", "燒賣", "腸粉"],
    義式: ["義式", "義大利", "披薩", "義大利麵"],
    美式: ["美式", "漢堡", "快餐", "薯條"],
    墨西哥: ["墨西哥", "塔可", "捲餅"],
    西式: ["西式", "早午餐", "沙拉", "可頌"],
    東南亞: ["東南亞", "泰式", "越南", "印尼", "馬來西亞"],
    印度: ["印度", "咖哩", "naan", "印度料理"],
  };

  const tags = [];
  for (const [tag, patterns] of Object.entries(tagMap)) {
    for (const p of patterns) {
      if (text.includes(p)) {
        tags.push(tag);
        break;
      }
    }
  }

  return tags;
}
